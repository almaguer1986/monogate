def update_private_repo():
    """Update private repo context and PAPER.md."""
    private = Path("D:/monogate-research")
    if not private.exists():
        return {"success": False, "error": "private repo not found"}

    try:
        # Append to PAPER.md
        paper = private / "PAPER.md"
        if paper.exists():
            with open(paper, "a", encoding="utf-8") as f:
                f.write("\n\n")
                f.write(FOUNDATIONS_CHAPTER)

        # Update CONTEXT.md header
        ctx = private / "CONTEXT.md"
        if ctx.exists():
            with open(ctx, "r", encoding="utf-8") as f:
                content = f.read()
            content = content.replace("v3.1.0", "v3.2.0", 1)
            with open(ctx, "w", encoding="utf-8") as f:
                f.write(content)

        # Git commit
        r1 = subprocess.run(["git", "add", "-A"], cwd=str(private), capture_output=True, text=True)
        r2 = subprocess.run(
            ["git", "commit", "-m",
             "feat: S80-S89 — tan(1) attack block, Claim C, literature bridge, Lean stub"],
            cwd=str(private), capture_output=True, text=True
        )
        r3 = subprocess.run(
            ["git", "tag", "-a", "v3.2.0", "-m", "v3.2.0: tan(1) obstruction formalized, Claim C"],
            cwd=str(private), capture_output=True, text=True
        )
        return {
            "success": True,
            "commit": r2.stdout.strip()[:100] or r2.stderr.strip()[:100],
            "tag": r3.returncode == 0,
        }
    except Exception as ex:
        return {"success": False, "error": str(ex)}


