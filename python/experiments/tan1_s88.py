"""
S88 — Literature Bridge Document

Exact theorems needed to close the tan(1) non-membership proof.
Maps the open gap to specific results in the transcendence theory literature.

GAP STATEMENT (from S86, S87):
  To prove tan(1) ∉ { Im(z)/Re(z) : z ∈ EML₁ }
  it suffices (and is necessary) to prove:
    (*) No z ∈ EML₁ has arg(z) = −1.
  By the propagation rule, this is equivalent to:
    (**) No z ∈ EML₁ has Im(z)/Re(z) = −tan(1).
  (same claim, just with arg = −1 vs ratio = −tan(1))

  This is self-referential. We need a GROUND TRUTH that breaks the circularity:
  a theorem saying that no combination of exp and log starting from 1 can produce
  an argument of exactly 1 radian.

WHAT THEOREMS WOULD CLOSE THE GAP:

THEOREM A (Baker-style, would close gap):
  If z₁,...,zₙ are nonzero and lie in a field F generated by {e^α : α algebraic} ∪ {π},
  and β₁,...,βₙ are algebraic, not all zero, then
  β₁·log(z₁) + ... + βₙ·log(zₙ) ≠ 0.

  RELEVANCE: If all EML₁ values were in such a field F, Baker would give control over
  their arguments. But EML₁ values are NOT in F (they involve e^z for z transcendental).

THEOREM B (Nesterenko + Structure, would close gap):
  If Im(EML₁) ⊆ ℤ·π ∪ (−π, 0), then 1 ∉ Im(EML₁) by:
  - 1 ≠ nπ for any integer n (Lindemann: π transcendental, π ≠ 1/n for any integer n)
  - 1 > 0, so 1 ∉ (−π, 0).

  RELEVANCE: From S85, at depth ≤ 5 all Im values are 0 or in (−π, 0). If this pattern
  persists to all depths, the claim follows from Lindemann alone.
  BUT: We showed in S86 that Im CAN become positive if arg(y) < 0. So the pattern
  might not persist.

THEOREM C (Schanuel, would close gap if proved):
  Schanuel's conjecture: if z₁,...,zₙ ∈ ℂ are ℚ-linearly independent,
  then trdeg_ℚ(z₁,...,zₙ, e^{z₁},...,e^{zₙ}) ≥ n.

  RELEVANCE: Consider z₁ = 1, z₂ = i. These are ℚ-linearly independent.
  Schanuel: trdeg_ℚ(1, i, e, e^i) ≥ 2. Since 1, i algebraic: trdeg_ℚ(e, e^i) ≥ 2.
  So e and e^i = cos(1)+i·sin(1) are algebraically independent over ℚ.
  Therefore cos(1) and sin(1) satisfy NO algebraic relation over ℚ(e) except sin²+cos²=1.

  For EML₁: elements involve towers like exp(exp(e)) etc. Under Schanuel, these would form
  an algebraically independent set, and tan(1) = sin(1)/cos(1) could not appear as
  Im(z)/Re(z) for any z built from exp and log starting from 1.

  STATUS: UNPROVED. Schanuel is one of the deepest open problems in transcendence theory.

THEOREM D (Conditional on Schanuel, explicit):
  Macintyre (1991) showed: the elementary theory of the real field with exp is decidable
  assuming Schanuel. This implies: any first-order statement about exp over ℝ is decidable.
  Our claim "∀ z ∈ EML₁, arg(z) ≠ 1" is such a statement (in a suitable encoding).
  Under Schanuel + decidability: the claim is either true or false, and provably so.

  RELEVANCE: Under Schanuel, T_i (i ∉ EML₁) is a decidable statement. The computation
  (S85: no witness at depth ≤ 5, 50907 values checked) strongly suggests it's TRUE.

THEOREM E (Lindemann-Weierstrass, partial):
  L-W: If α₁,...,αₙ are distinct algebraic numbers, then e^{α₁},...,e^{αₙ} are
  linearly independent over the algebraic numbers.

  RELEVANCE: e = e^1 is transcendental. e^e is transcendental (Hermite-Lindemann, α=e but
  wait: e is transcendental so L-W doesn't directly say e^e is transcendental...).
  Actually e^e: by Gelfond-Schneider, α^β with α=e (transcendental), so G-S doesn't apply.
  By Lindemann: e^α transcendental for algebraic α≠0. e is transcendental so e^e isn't
  directly covered. The question of whether e^e is transcendental is OPEN.

IMMEDIATE CONSEQUENCE (if Schanuel holds):
  Under Schanuel: the values in EML₁ form a "tower" with algebraically independent levels.
  Specifically: e, e^e, e^(e^e), ... are all algebraically independent.
  The arguments arg(z) for z ∈ EML₁ are all transcendental but algebraically independent
  of tan(1) = sin(1)/cos(1).
  Therefore arg(z) ≠ 1 for all z ∈ EML₁.
  T_i follows.

OPEN STATUS TABLE:
  Theorem   | Status    | Closes Gap? | Reference
  L-W       | PROVED    | NO (partial)| Lindemann 1882, Weierstrass 1885
  Baker     | PROVED    | NO (direct) | Baker 1966, 1975
  Nesterenko| PROVED    | NO (direct) | Nesterenko 1996
  G-S       | PROVED    | NO (direct) | Gelfond 1934, Schneider 1934
  Schanuel  | OPEN      | YES         | Schanuel ~1960s (conjecture)
  Structure | OPEN      | YES (if Im⊆(−π,0]) | Claim, unproved

RECOMMENDED PATH:
  1. Prove the structural claim: Im(EML₁) ⊆ (−π, 0] (all Im-parts non-positive).
     If true, then Im(z) = 1 is impossible directly (1 > 0 and 1 ∉ (−π, 0]).
  2. The structural claim would follow from:
     Lemma: If Im(x), Im(y) ≤ 0 and arg(y) ≤ 0, then
            Im(eml(x,y)) = exp(Re(x))·sin(Im(x)) − arg(y) might still be positive.
     Counter: Im(eml(0, y)) = 0 − arg(y) = −arg(y) ≥ 0 when arg(y) ≤ 0!
     So: starting from x=0 (which is achievable in principle), we can get Im > 0.
  3. CONCLUSION: Im(EML₁) ⊆ (−π, 0] is FALSE in general (counterexample: need x ∈ EML₁
     with Re(x) = 0 and arg(y) ≤ 0 — Re(x) = 0 would require x pure imaginary).
     x = 0 ∉ EML₁ (starts at 1). What's the smallest non-zero real Re(x)?
     Re(eml(1,1)) = e ≈ 2.718. So Re(x) > 0 for all x ∈ EML₁ with Im(x) = 0.
     (By induction: Re ≥ 1 always? Unclear.)
"""

